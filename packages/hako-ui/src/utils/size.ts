export const xsLayoutColumnToClassName = (size: number) => {
  switch (size) {
    case 1:
      return 'w-1/12';
    case 2:
      return 'w-1/6';
    case 3:
      return 'w-1/4';
    case 4:
      return 'w-1/3';
    case 5:
      return 'w-5/12';
    case 6:
      return 'w-1/2';
    case 7:
      return 'w-7/12';
    case 8:
      return 'w-2/3';
    case 9:
      return 'w-3/4';
    case 10:
      return 'w-5/6';
    case 11:
      return 'w-11/12';
    default:
      return 'w-full';
  }
};

